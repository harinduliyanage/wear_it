package com.ijse.wearit.model;

/**
 *
 * @author Harindu.sul
 */
public class Status {
    private int code;
    private String status;
    private String msg;

    public Status(int code, String status, String msg) {
        this.code = code;
        this.status = status;
        this.msg = msg;
    }

    public Status() {
        
    }
    

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the msg
     */
    public String getMsg() {
        return msg;
    }

    /**
     * @param msg the msg to set
     */
    public void setMsg(String msg) {
        this.msg = msg;
    }
    
}
